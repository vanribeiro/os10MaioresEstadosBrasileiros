package challenge;

public class State {
	
	private String Uf;
	private String name;
	
	public State(String Uf, String name) {
		this.Uf = Uf;
		this.name = name;
	}
	
	public String getUf() {
		return Uf;
	}
	public void setUf(String uf) {
		Uf = uf;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
